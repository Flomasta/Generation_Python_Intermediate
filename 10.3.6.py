words = [i.strip('.,!?:;-') for i in input().lower().split()]
res = {}
for word in words:
    res[word] = res.get(word, 0) + 1
min_key = min(res.values())
min_word = sorted([k for k, v in res.items() if v == min_key])[0]
print(min_word)


