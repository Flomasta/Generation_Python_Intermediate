secret_word = input()
n = int(input())
d = {}
res = ''
d_symb = {}
for i in range(n):
    k, v = input().split(':')
    d[int(v)] = k

for i in set(secret_word):
    d_symb[i] = secret_word.count(i)

for i in secret_word:
    res += d[d_symb[i]]

print(res)

