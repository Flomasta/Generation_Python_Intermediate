def merge(values):  # values - —ç—Ç–æ —Å–ø–∏—Å–æ–∫ —Å–ª–æ–≤–∞—Ä–µ–π
    res = {}
    for i in values:
        for key, value in i.items():
            res.setdefault(key, set()).add(value)
    # for k, v in res.items():
    #     res[k] = set(sorted((map(int, v))))
    return res


