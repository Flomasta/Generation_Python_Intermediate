with open('logfile.txt', 'r', encoding='utf-8') as logfile:
    data = logfile.readlines()
    for i in data:
        name, time_in, time_out = i.split(',')
        hour_in, minutes_in = list(map(int, time_in.split(':')))
        hour_out, minutes_out = list(map(int, time_out.split(':')))
        diff = ((hour_out * 60 + minutes_out) - (hour_in * 60 + minutes_in))
        if diff >= 60:
            with open('output_names.txt', 'a', encoding='utf-8') as output:
                print(name, file=output)
        # print(name)

