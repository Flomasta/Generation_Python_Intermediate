file = open('../txt_files_for_part_files_and_files_exam/prices.txt', 'r', encoding='utf-8')
lst = []
for line in file:
    data = line.split('\t')
    amount, price = int(data[-2].strip()), int(data[-1].strip())
    lst.append(amount * price)
print(sum(lst))
file.close()

