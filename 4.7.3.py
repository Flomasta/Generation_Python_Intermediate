from copy import deepcopy

n = int(input())
mtx = [list(map(int, input().split())) for i in range(n)]
pwr = int(input())
mask = deepcopy(mtx)

while pwr > 1:
    res = [[0] * n for _ in range(n)]
    for i in range(n):
        for j in range(n):
            for q in range(n):
                res[i][j] += mtx[i][q] * mask[q][j]

    pwr -= 1
    mtx = deepcopy(res)
[print(*i) for i in mtx]


