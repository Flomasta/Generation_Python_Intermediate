n = int(input())
mtx = [list(map(int, input().split())) for j in range(n)]
max_num = mtx[0][n - 1]
for i in range(n):
    for j in range(n):
        if i >= n - 1 - j and max_num < mtx[i][j]:
            max_num = mtx[i][j]
print(max_num)

