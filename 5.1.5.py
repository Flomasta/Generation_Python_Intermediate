n = int(input())
mtx = [list(map(int, input().split())) for j in range(n)]
flag = False
for i in range(n):
    for j in range(n):
        if j != n - i - 1:
            if mtx[j][i] == mtx[~i][~j]:
                flag = True
            else:
                print('NO')
                break
    else:
        continue
    break
if flag:
    print('YES')

