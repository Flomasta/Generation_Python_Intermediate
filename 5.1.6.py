n = int(input())
mtx = [list(map(int, input().split())) for j in range(n)]
mask = [i for i in range(1, n + 1)]
flag = False

for i in mtx:
    if set(mask) != set(i):
        break
else:
    for i in zip(*mtx):
        if set(mask) != set(i):
            break
    else:
        flag = True
print('YES' if flag else 'NO')

