n = int(input())
mtx = [['.' for i in range(n)] for j in range(n)]
for i in range(n):
    for j in range(n):
        if i == j or j == n - i - 1 or j == int(n / 2) or i == int(n / 2):
            mtx[i][j] = '*'
[print(*i) for i in mtx]

