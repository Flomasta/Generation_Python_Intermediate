n = 8
board = [['.'] * n for i in range(n)]

x, y = input()
x = ord(x) - 97
y = n - int(y)
for i in range(n):
    for j in range(n):
        if abs(i - y) == abs(j - x) or i == y or j == x:
            board[i][j] = '*'
board[y][x] = 'Q'

[print(*i) for i in board]

