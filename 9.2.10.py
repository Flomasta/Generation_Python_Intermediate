list_of_pupils = [input() for _ in range(n + m)]
counter = n + m
for i in set(list_of_pupils):
    pupil_in_list = list_of_pupils.count(i)
    if pupil_in_list > 1:
        counter -= pupil_in_list

print(counter if counter else 'NO')


